package com.example.justdoit;

public class gsconstructor {
    private String des;
    private int image;

    // Constructor
    public gsconstructor(String des, int image) {
        this.des = des;
        this.image = image;
    }

    // Getter and Setter
    public String getdes() {
        return des;
    }

    public void setdes(String des) {
        this.des = des;
    }

    public int getimage() {
        return image;
    }

    public void setimage(int image) {
        this.image = image;
    }
}


